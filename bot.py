ACCURACY_LABEL = {
    "точно": "🟢 Конкретный город",
    "приблизительно": "🟡 Приблизительно (субрегион)",
    "нет": "🔴 Точный город не определяется по этому коду",
}


def lookup(phone_digits: str):
    prefix = phone_digits[:3]
    if prefix in MOBILE_CODES:
        return prefix, MOBILE_CODES[prefix]
    return None


def normalize_phone(raw: str):
    digits = re.sub(r"\D", "", raw)
    if len(digits) == 11 and digits[0] in ("7", "8"):
        return digits[1:]
    if len(digits) == 10:
        return digits
    return None


def format_population(n: int) -> str:
    if n >= 1_000_000:
        return f"{n / 1_000_000:.1f} млн чел."
    return f"{n // 1_000} тыс. чел."


def get_local_time(tz_name: str) -> str:
    try:
        tz = zoneinfo.ZoneInfo(tz_name)
        now = datetime.now(tz)
        offset = now.strftime("%z")
        offset_fmt = f"UTC{offset[:3]}:{offset[3:]}"
        return f"{now.strftime('%H:%M')} ({offset_fmt})"
    except Exception:
        return "н/д"


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Привет! Отправь мобильный номер РФ - скажу город, оператора и местное время.\n\n"
        "Форматы: +7 916 123 45 67 / 89161234567 / 9161234567\n\n"
        "🟢 - конкретный город\n"
        "🟡 - приблизительно (код охватывает несколько городов)\n"
        "🔴 - точный город не определяется (код федеральный)"
    )


async def handle_phone(update: Update, context: ContextTypes.DEFAULT_TYPE):
    raw = update.message.text.strip()
    digits = normalize_phone(raw)

    if not digits:
        await update.message.reply_text(
            "Не похоже на российский номер. Попробуй: +7 916 1234567 или 89161234567"
        )
        return

    if digits[0] != "9":
        await update.message.reply_text(
            "Этот бот работает только с мобильными номерами (начинаются на 9).\n"
            "Стационарные коды (495, 812 и т.д.) не поддерживаются."
        )
        return

    result = lookup(digits)

    if not result:
        await update.message.reply_text(
            f"Код `{digits[:3]}` не найден в базе.",
            parse_mode="Markdown"
        )
        return

    prefix, (city, population, timezone, operator, accuracy) = result

    lines = [
        f"📞 Номер: `{raw}`",
        f"🔢 Код: `{prefix}`",
        f"📡 Оператор: *{operator}*",
        f"📍 Город (регион регистрации): *{city}*",
        f"👥 Население: *{format_population(population)}*",
        f"🕐 Местное время: *{get_local_time(timezone)}*",
        f"📊 Точность: {ACCURACY_LABEL[accuracy]}",
        "",
        "⚠️ _Регион выдачи номера при подключении. После 2013 г. можно сменить оператора, сохранив номер._",
    ]

    await update.message.reply_text("\n".join(lines), parse_mode="Markdown")


def main():
    token = os.environ.get("BOT_TOKEN")
    if not token:
        raise RuntimeError("BOT_TOKEN не задан.")

    app = ApplicationBuilder().token(token).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_phone))

    print("Бот запущен. Ctrl+C для остановки.")
    app.run_polling()


if __name__ == "__main__":
    main()
